@dataclass
class LongitudinalParams:
    FZ0: float = 1500.0 # Same reference load as Fy
    # Peak (D) load & camber dependence
    pDx1: float = 1.0   # Peak friction coefficient Fx at Fz0
    pDx2: float = 0.0   # Variation of Fx peak friction with load
    pDx3: float = 0.0   # Variation of Fx peak friction with squared camber (often small/zero)
    # Shape factor
    pCx1: float = 1.65  # Shape factor Fx
    # Stiffness (K) baseline and dependencies -> used to compute B
    pKx1: float = 20.0  # Longitudinal slip stiffness Kx/Fz at Fz0
