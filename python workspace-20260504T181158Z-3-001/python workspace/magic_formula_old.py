    pKx3: float = 0.0   # Exponent in slip stiffness Kx/Fz with load
    # Curvature (E) dependencies
    pEx1: float = 0.0   # Longitudinal curvature Efx at Fznom
    pEx2: float = 0.0   # Variation of curvature Efx with load
    pEx3: float = 0.0   # Variation of curvature Efx with load squared
    pEx4: float = 0.0   # Factor in curvature Efx while driving/braking (sign(kappa))
    # Horizontal/vertical shifts
    pHx1: float = 0.0   # Horizontal shift Shx at Fznom
    pHx2: float = 0.0   # Variation of shift Shx with load
