def updateSmallestError(value, error, smallestErrorValue, smallestError):
    if error < smallestError:
            smallestError = error
            smallestErrorValue = value
    return smallestErrorValue, smallestError


def snapToValues(value, options):
    snapedValue = options[0]
    smallestError = abs(options[0] - value)
    for option in options:
        error = abs(option - value)
        snapedValue, smallestError = updateSmallestError(option, error, snapedValue, smallestError)
        
    return snapedValue

