def clamp_to_nearest(df, col_name, allowed_vals):
    # Ensure allowed_vals is a sorted numpy array
    allowed = np.sort(np.array(list(allowed_vals)))
    
    # Get the raw values from the column
    data = df[col_name].values
    
    # Find where data would be inserted (indices)
    idx = np.searchsorted(allowed, data, side='left')
    
    # Prevent out-of-bounds indices
    # (idx 0 stays 0, idx len(allowed) becomes len(allowed)-1)
    idx_right = np.clip(idx, 0, len(allowed) - 1)
    idx_left = np.clip(idx - 1, 0, len(allowed) - 1)
    
    # Use np.take to safely grab the actual values from the target set
    val_right = np.take(allowed, idx_right)
    val_left = np.take(allowed, idx_left)
    
    # Calculate which one is closer for each row
    # np.abs handles the entire array at once
    dist_left = np.abs(data - val_left)
    dist_right = np.abs(data - val_right)
    
    # If dist_left is smaller, take val_left, otherwise val_right
    clamped = np.where(dist_left <= dist_right, val_left, val_right)
    
    return clamped

