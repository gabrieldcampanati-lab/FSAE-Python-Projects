def detect_header(path):
    with open(path, "r", errors="ignore") as f:
        for i, line in enumerate(f):
            if "\t" in line:
                toks = line.rstrip("\n").split("\t")
                if {"ET","V","SA","IA","FX","FY","FZ","MZ","SR"}.issubset(set(toks)):
                    return i, toks
    raise RuntimeError("No Round-9 header in: " + path)

def read_round9_run_keep_all(path):
    """Read TTC Round-9 Run file, keep ALL columns, add canonical duplicates."""
    hdr_idx, names = detect_header(path)
    df = pd.read_csv(path, sep="\t", names=names, skiprows=hdr_idx+2, engine="python")
    # drop any trailing-tab anonymous columns
    df = df.loc[:, ~df.columns.str.startswith("Unnamed")]

    # ---- Add canonical duplicates (do NOT drop originals) ----
    # time
    if "ET" in df.columns and "time" not in df.columns:
        df["time"] = pd.to_numeric(df["ET"], errors="coerce")

    # longitudinal slip ratio (kappa)
    if "SR" in df.columns and "KAPPA" not in df.columns:
        df["KAPPA"] = pd.to_numeric(df["SR"], errors="coerce")

    # temperatures → keep TSTI/TSTC/TSTO if present and add TIRE_TEMP (median)
    for tcol in ["TSTI","TSTC","TSTO","RST"]:
        if tcol in df.columns:
            df[tcol] = pd.to_numeric(df[tcol], errors="coerce")
    if all(c in df.columns for c in ["TSTI","TSTC","TSTO"]):
        df["TIRE_TEMP"] = df[["TSTI","TSTC","TSTO"]].median(axis=1)

    # Ensure core numeric columns are numeric (but DO NOT drop any other string cols)
    for c in ["FX","FY","FZ","MX","MY","MZ","SA","IA","V","P"]:
        if c in df.columns:
            df[c] = pd.to_numeric(df[c], errors="coerce")

    return df

def classify_test_type(df):
    """Heuristic: look at spans of SA and KAPPA in THIS run."""
    sa = pd.to_numeric(df["SA"], errors="coerce").dropna() if "SA" in df else pd.Series(dtype=float)
    kp = pd.to_numeric(df["KAPPA"], errors="coerce").dropna() if "KAPPA" in df else pd.Series(dtype=float)
    sa_span = (sa.max()-sa.min()) if len(sa) else 0.0
    kp_span = (kp.max()-kp.min()) if len(kp) else 0.0
    sa_big = sa_span > np.deg2rad(6)     # ~±6°
    kp_big = kp_span > 0.16              # ~±6%
    if sa_big and not kp_big: return "lateral_pure"
    if kp_big and not sa_big: return "longitudinal_pure"
    if sa_big and kp_big:     return "combined"
    return "unknown"

def summarize_setpoints(df):
    """Run-level medians for convenience (kept as extra columns)."""
    def med(col):
        return float(np.nanmedian(pd.to_numeric(df[col], errors="coerce"))) if col in df else np.nan
    return {
        "FZ_set": med("FZ"),
        "IA_set": med("IA"),
        "V_set":  med("V"),
        "P_set":  med("P"),
        "T_set":  med("TIRE_TEMP") if "TIRE_TEMP" in df else np.nan,
    }

# ------------ build master (keeping ALL columns) ------------
def buildMaster(folder, outMaster, outBlockGuide):
    rows, meta = [], []
    for path in sorted(glob.glob(os.path.join(folder,"*.dat"))):
        run_id = Path(path).stem
        try:
            df = read_round9_run_keep_all(path)
            ttype = classify_test_type(df)
            sp = summarize_setpoints(df)

            # Create a new 'block_id' column
            # It increments by 1 every time the gap in 'ET' from the previous row is > 1.0s

            # attach run metadata (without dropping any original columns)
            df["run_id"] = run_id
            df["test_type"] = ttype
            df["FZ_set"], df["IA_set"], df["V_set"], df["P_set"], df["T_set"] = \
                sp["FZ_set"], sp["IA_set"], sp["V_set"], sp["P_set"], sp["T_set"]

            rows.append(df)
            meta.append({"run_id": run_id, "test_type": ttype, **sp, "rows": len(df.columns)})
            print(f"Added {run_id}: {ttype}, cols={len(df.columns)}, rows={len(df)}")
        except Exception as e:
            print(f"Failed on {run_id}: {e}")

    master = pd.concat(rows, ignore_index=True) if rows else pd.DataFrame()
    master["block_id"] = (master["ET"].diff() > 1).cumsum()
    master["fz_clamped"] = clamp_to_nearest(master, "FZ", fzTestValues)
    rows2 = []
    columns = ["ET", "V", "N", "SA", "IA", "RL", "RE", "P", "FX", "FY", "FZ", "MX", "MZ", "NFX", "NFY", "RST", "TSTI", "TSTC", "TSTO", "AmbTemp", "SR", "time", "KAPPA", "TIRE_TEMP", "run_id", "test_type", "FZ_set", "IA_set", "V_set", "P_set", "T_set", "block_id"]
    for i in range(0, max(master["block_id"])):
        row = master[master["block_id"] == i].iloc[0]
        fz = snapToValues(row["FZ"], fzTestValues)
        inclinationAngle = snapToValues(row["IA"], inclinationAngleTestValues)
        pressure = snapToValues(row["P"], pressureTestValues)
        velocity = snapToValues(row["V"], velocityTestValues)
        row["FZ"] = fz
        row["IA"] = inclinationAngle
        row["P"] = pressure
        row["V"] = velocity
        print(row)
        rows2.append(row)
    blockDataFrame = pd.DataFrame(rows2, columns=columns)
    master.to_csv(outMaster, index=False, sep = "\t")
    blockDataFrame.to_csv(outBlockGuide, index=False, sep = "\t")
    print(f"\nSaved (ALL COLS): {outMaster}  rows={len(master)}, cols={len(master.columns)}")